# Declara dos variables numéricas, realiza las operaciones de suma, resta, multiplicación y división, y muestra los resultados en la consola.
num1 = 10
num2 = 5

# Suma
print("Suma: " +str(int(num1) + num2))

# Resta
print("Resta: " +str(int(num1) - num2))

# Multiplicación
print("Multiplicación: " +str(int(num1) * num2))

# División
print("División: " +str(int(num1) / num2))
