# Solicita al usuario dos números y realiza las operaciones suma, resta, multiplicación y
# división.

num1 = int(input("Número 1: "))
num2 = int(input("Número 2: "))

# Suma
print("Suma: " +str(int(num1) + num2))

# Resta
print("Resta: " +str(int(num1) - num2))

# Multiplicación
print("Multiplicación: " +str(int(num1) * num2))

# División
print("División: " +str(int(num1) / num2))